    static var ypWhite = UIColor(named: "YP White") ?? UIColor.white
    static var ypWhiteA50 = UIColor(named: "YP White Alpha 50") ?? UIColor.white.withAlphaComponent(50)
    static var ypRed = UIColor(named: "YP Red") ?? UIColor.red
    static var ypGreen = UIColor(named: "YP Blue") ?? UIColor.green
